# engine/product/runtime_bridge.py
"""E5 — Runtime Bridge: observability, error explanation, and dependency automation.

E5a — ace_instrument(stack):
  Reads stack from ACE capabilities → generates production-ready OTel
  configuration files (Python ASGI middleware, Node.js SDK config, structured
  logging, Jaeger/OTLP export). Output is ready to plug into the framework
  entry point with minimal manual wiring.

E5b — ace_explain_error(error, stack_trace):
  Parses stack trace → identifies affected modules → loads relevant ACE
  decisions and intelligence → LLM explains the error in plain English with
  fix commands. Checks runbook table for known patterns. Auto-captures new
  error patterns as runbooks for future sessions.

E5c — ace_update_deps(strategy, repo_path):
  Runs pip-audit / npm audit → cross-references with ACE decision graph →
  returns a risk-annotated update plan. Decisions that pin specific versions
  or mention a package block that update with a reason.

Closes:
  #6 Knowledge Gap at Errors  C→A
  #10 Debugging Impossible  C+→A
  #5 Dependency Hell  C-→A+
"""

