async def _detect_stack(product_id: str) -> list[str]:
    """Detect stack from capabilities."""
    try:
        async with pool.connection() as db:
            rows = parse_rows(
                await db.query(
                    "SELECT slug, title, category FROM capability WHERE product = <record>$product AND status != 'deprecated' LIMIT 30",
                    {"product": product_id},
                )
            )
        from core.engine.product.generation_engine import _infer_stack_from_capabilities

        return _infer_stack_from_capabilities(rows)
    except Exception:
        return ["python"]  # safe default
